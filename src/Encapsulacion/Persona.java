package Encapsulacion;

	public class Persona {
		String nombre;
		String apellido; 
		int edad;
		
		public Persona(String nombre, String apellido, int edad) {
			super();
			this.nombre = nombre;
			this.apellido = apellido;
			this.edad = edad;
			
		}
   public void mostrar() {
   System.out.print("Nombre: "+ nombre + "Apellido: " + apellido + "Edad:" + edad );
			
		}
			
		}

		
	


